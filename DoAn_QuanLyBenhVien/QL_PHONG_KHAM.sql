);
GO

CREATE TABLE NHANVIEN (
    MaNV VARCHAR(15) PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL,
    ChucVu NVARCHAR(50) NOT NULL CHECK (ChucVu IN (N'Bác sĩ', N'Dược sĩ', N'Lễ tân', N'Thu ngân')),
    MaCK VARCHAR(15) NULL,
