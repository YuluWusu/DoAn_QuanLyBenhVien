    CONSTRAINT CK_BACSI CHECK (ChucVu != N'Bác sĩ' OR MaCK IS NOT NULL)
);
GO
CREATE TABLE QUYEN (
    MaQuyen VARCHAR(10) PRIMARY KEY,
    TenQuyen NVARCHAR(50) NOT NULL -- 'Admin', 'User'
);
GO
CREATE TABLE TAIKHOAN (
    TenDangNhap VARCHAR(50) PRIMARY KEY,
    MatKhau VARCHAR(255) NOT NULL, 
    MaNV VARCHAR(15) UNIQUE,       
    MaQuyen VARCHAR(10),           -- Khóa ngoại tới bảng QUYEN
    TrangThai NVARCHAR(20) DEFAULT N'Hoạt động',
    CONSTRAINT FK_TK_NV FOREIGN KEY (MaNV) REFERENCES NHANVIEN(MaNV) ON DELETE CASCADE,
    CONSTRAINT FK_TK_QUYEN FOREIGN KEY (MaQuyen) REFERENCES QUYEN(MaQuyen)
